var SortService = class {
  constructor(settings) {
    this.settings = settings;
  }
  sort(cards, mode, ascending) {
    const sorted = [...cards];
    switch (mode) {
      case "manual":
        return this.sortManual(sorted, ascending);
      case "created":
        return sorted.sort((a, b) => {
          const primary = ascending ? a.created - b.created : b.created - a.created;
          if (primary !== 0)
            return primary;
          const am = typeof a.modified === "number" ? a.modified : a.created;
          const bm = typeof b.modified === "number" ? b.modified : b.created;
          const secondary = ascending ? am - bm : bm - am;
          if (secondary !== 0)
            return secondary;
          return ascending ? a.content.localeCompare(b.content) : b.content.localeCompare(a.content);
        });
      case "modified":
        return sorted.sort((a, b) => {
          const am = typeof a.modified === "number" ? a.modified : a.created;
          const bm = typeof b.modified === "number" ? b.modified : b.created;
          const primary = ascending ? am - bm : bm - am;
          if (primary !== 0)
            return primary;
          const secondary = ascending ? a.created - b.created : b.created - a.created;
          if (secondary !== 0)
            return secondary;
          return ascending ? a.content.localeCompare(b.content) : b.content.localeCompare(a.content);
        });
      case "alpha":
        return sorted.sort(
          (a, b) => ascending ? a.content.localeCompare(b.content) : b.content.localeCompare(a.content)
        );
      case "status":
        return sorted.sort((a, b) => {
          const list = this.settings.cardStatuses || [];
          const indexFor = (c) => {
            if (!c.status)
              return Infinity;
            const idx = list.findIndex((s) => (s.name || "").toLowerCase() === (c.status.name || "").toLowerCase());
            return idx >= 0 ? idx : Infinity;
          };
          const ai = indexFor(a);
          const bi = indexFor(b);
          if (ai !== bi)
            return ascending ? ai - bi : bi - ai;
          return ascending ? a.content.localeCompare(b.content) : b.content.localeCompare(a.content);
        });
      default:
        return sorted;
    }
  }
  sortManual(cards, ascending) {
    const order = this.settings.manualOrder || [];
    const orderMap = new Map(order.map((id, i) => [id, i]));
    return cards.sort((a, b) => {
      if (a.pinned && !b.pinned)
        return -1;
      if (!a.pinned && b.pinned)
        return 1;
      const aIdx = orderMap.has(a.id) ? orderMap.get(a.id) : Infinity;
      const bIdx = orderMap.has(b.id) ? orderMap.get(b.id) : Infinity;
      if (aIdx !== bIdx)
        return aIdx - bIdx;
      return ascending ? a.created - b.created : b.created - a.created;
    });
  }
};

